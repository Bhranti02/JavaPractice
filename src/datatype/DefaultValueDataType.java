package datatype;

public class DefaultValueDataType {
    // declare static data type outside main method for default value and sout(print command) in main method
   static boolean b ;
   static char c ;
   static byte x  ;
  static short s ;
   static int g ;
   static long l;
   static float f ;
   static double d ;
   static String y ; // S capital



    public static void main(String[] args) {
        System.out.println(b);
        System.out.println(c);
        System.out.println(x);
        System.out.println(s);
        System.out.println(g);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(y);
    }
}
