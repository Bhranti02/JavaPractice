package operators.forLoop;

public class ForLoopPrint1To10 {

    public static void main(String[] args) {
        for (int a =1; a<= 10; a++){ // first initialize variable,check condition,execute program then increase it
                                     // if a<=100, then will print 1 to 100
            System.out.println(a);
        }
    }

}
