package operators.forLoop;

public class FindEvenNumber {
    public static void main(String[] args) {
        int a ;
        for(a = 2; a<=20; a+=2){  // change condition a<=100, to find even number till 100
                                 // for reverse a=20; a>=1; a-=2 // for odd number change a =19 or 1
            System.out.println(a);
        }
    }
}
